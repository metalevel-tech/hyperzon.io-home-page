                <h1 class="single-post-title">
                    The Ultimate Guide To Using Amazon Ads Console’s Bulk Operations
                </h1>

                <p class="single-post-subtitle">
                    What exactly are the Amazon PPC Bulk Operations? And how will this tool help you manage your campaigns faster and more efficiently?
                </p>
            </div>

            <div class="single-post-content-section">
                <h2>
                    So, What Exactly Are The Amazon PPC Bulk Operations?
                </h2>

                <p>
                    If we need to summarize Bulk Operations with just a few words—it’s a way to manage and optimize your existing campaigns and create new ones at scale using spreadsheets. By using <a href="#">bulksheets</a>, you can make all kinds of changes to your campaigns, placements, keywords, and bids without the need to go through each campaign separately from the dashboard. As Amazon puts it, "Use spreadsheets to make large-scale updates to your Sponsored Products, Sponsored Brands, and Sponsored Display campaigns."
                </p>
                <p>
                    To access the <b>Amazon PPC Bulk Operations</b> options, you need to go to your Amazon Advertising Console (The updated name for the Campaign Manager), go to Sponsored ads in the sidebar, and click on Bulk operations.
                </p>
                <img src="/public/blog/test-post/test_post_img_1.webp" alt="Post image">
                <p>
                    <em><b>NOTE:</b> <b>"Bulksheet"</b> is Amazon’s terminology for a spreadsheet that contains your campaign’s data and gives you the possibility to make changes in bulk.</em>
                </p>
                <h2>
                    How To Get Started With Bulk Operations And Download Your Bulksheet File?
                </h2>
                <p>
                    To get started with Bulk Operations, you must create and download your custom spreadsheet. You need to select the date range for the performance metrics and click which ones you don’t need to be included. Once downloaded, the spreadsheet will contain three tabs – one tab for each different type of campaign – <b>Sponsored Products</b>, <b>Sponsored Brands</b>, and <b>Sponsored Display</b>, as each type has different columns.
                </p>
                <img src="/public/blog/test-post/test_post_img_2.webp" alt="Post image">
                <p>
                    We recommend you keep the default metric options. But let’s break them down in case you want to exclude some of them:
                </p>
                <ul>
                    <li>
                        <p>
                            <b>Date range</b>
                        </p>
                        <p>
                            The date range for the performance metrics. You can select a custom date range or use the default one (last 30 days).
                        </p>
                    </li>
                    <li>
                        <p>
                            <b>Terminated campaigns</b>
                        </p>
                        <p>
                            The bulksheet file does not include the terminated campaigns. If you want to exclude them in the sheet, you’ll need to uncheck this. Terminated campaigns are those that have been ended, stopped, deleted, or rejected.
                        </p>
                    </li>
                    <li>
                        <p>
                            <b>Campaign items with zero impressions</b>
                        </p>
                        <p>
                            If this option is unchecked, the bulksheet file will also include rows of data with zero impressions. Depending on the use case, you will want to have this checked or unchecked:
                        </p>
                        <ul>
                            <li>
                                <p>
                                    <b>I If your campaigns do not have any impressions</b> and you want to bulk- increase the bids, you’ll want to have this unchecked. You need to see the keywords with zero impressions to increase their bids.
                                </p>
                            </li>
                            <li>
                                <p>
                                    <b>If you want to optimize your campaign</b>, e.g., lower some bids or move converting keywords among campaigns, you can check this so you have a spreadsheet with fewer data.
                                </p>
                            </li>
                            <li>
                                <p>
                                    <b>Placement data for campaigns</b> – This option, by default, is unchecked. So you will see and be able to change the placement options for your campaign. You can change the % increase of bids for Top of search (page one), Rest of search, and Product pages.
                                </p>
                            </li>
                            <li>
                                <p>
                                    <b>Brand assets data</b> – If this option is unchecked, you will see a tab containing your Brand Assets.
                                </p>
                            </li>
                        </ul>
                    </li>
                </ul>
                <h2>
                    The Next Steps
                </h2>
                <p>
                    After you download the spreadsheet with all the information for the campaigns, you can filter it further and see only the data you want.
                </p>
                <p>
                    For example, if you want to adjust only the campaign daily budgets, you can filter it by "Record Type" and select only "Campaign." That way, you will see only the rows with the top-level campaign data.
                </p>
                <img src="/public/blog/test-post/test_post_img_3.webp" alt="Post image">
                <p>
                    Or, if you want to adjust the different placements, you can filter it by "Record Type" and select both Campaign and Campaign By Placement.
                </p>
                <p>
                    <em><b>PRO TIP:</b> When using bulksheets, messing things up is easy. So if you want to add a new Sponsored Products campaign, you can delete all other tabs and leave only the SP one.</em>
                </p>
                <h2>
                    Why Are Bulk Operations So Useful, And What Are Some Of The Different Usage Cases?
                </h2>
                <p>
                    Short answer – because they are very flexible and save us a lot of time. You can move keywords around campaigns, create new campaigns, and adjust bids directly from the spreadsheet without the need to open multiple tabs in your browser and copy/paste keywords between them.You are already starting to understand what Bulk Operations are used for. But let’s go through some specific use cases where they can be handy.
                </p>
                <p>
                    <b>Bulksheet files are helpful when you have multiple campaigns, and you want to:</b>
                </p>
                <ul>
                    <li>
                        Bulk add multiple new campaigns, ad groups, and targets without the need to create them one by one from the Advertising Console.
                    </li>
                    <li>
                        Update in bulk the status of campaigns and ad groups.
                    </li>
                    <li>
                        Bulk update the Match Types.
                    </li>
                    <li>
                        Update in bulk the bidding strategy between all the campaigns.
                    </li>
                    <li>
                        Bulk update the Campaign Budgets – this is very useful when there are special events like Black Friday and Prime Day.
                    </li>
                    <li>
                        Move keywords between different campaigns and ad groups or move them to new campaigns.
                    </li>
                </ul>
                <p>
                    <em><b>PRO TIP:</b> Each bulksheet file needs to be processed by Amazon row by row. To reduce the time necessary for processing, you can delete all non-modified rows before uploading the file.</em>
                </p>
                <h2>
                    Managing Your Sponsored Products Ads With Bulksheet File
                </h2>
                <p>
                    To create a new campaign via Bulk Operations, you need to follow its template format (the Sponsored Products tab in the downloaded sheet) to create the campaign successfully. This applies to Sponsored Brands and Sponsored Display, too.
                </p>
                <p>
                    Depending on the type of campaign/ad group/ad/target you want to create, the structure of the bulk file will be different. So the easiest way to do that is by copy/pasting an already existing campaign and modifying it.
                </p>
                <p>
                    When creating a Sponsored Products campaign with the bulksheet file, you can create and manage Automatic and Manual Campaigns. Depending on which type of campaign you want to create, the required fields in the sheet will be different.
                </p>
                <p>
                    You can see the <b>Sponsored Products</b> required fields in the hierarchy tree below.
                </p>
                <img src="/public/blog/test-post/test_post_img_4.webp" alt="Post image">
                <p>
                    Or, if you want to adjust the different placements, you can filter it by "Record Type" and select both "Campaign" and "Campaign By Placement".
                </p>
